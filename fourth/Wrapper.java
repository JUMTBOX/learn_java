package fourth;

public class Wrapper {
    public static void main(String[] args) {
        Integer num = 1;
        Long num2 = 2L;
        Float num3 = 3f;
        Double num4 = 4d;
        Boolean yes = true;

        System.out.println(num);
        System.out.println(num2);
        System.out.println(num3);
        System.out.println(num4);
        System.out.println(yes);
    }
}
