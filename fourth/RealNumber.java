package fourth;

public class RealNumber {
    public static void main(String[] args) {
        //float 형의 변수를 선언할때는 value 뒤에 f를 붙여줘야함
        //자바에서는 기본적으로 소수점이 들어간 숫자는 double타입으로 간주하기 때문
        float floatNum=1.2f;
        double doubleNum=4.3;

        System.out.println(floatNum);
        System.out.println(doubleNum);
    }
}
